    return Scaffold(
      appBar: AppBar(title: Text('Home')),
      body: Center(
        child: Text(
          "Welcome",
          style: TextStyle(fontSize: 32, fontWeight: FontWeight.bold),
        ),
      ),
    );
  }
}
